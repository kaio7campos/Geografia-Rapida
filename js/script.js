// 6) Escreva  um  algoritmo  para  ler  as  dimensões  de  um  retângulo
// (base  e  altura),  calcular  e  escrever  a área do retângulo.
function area() {
    let base = parseInt(prompt('Digite a base:'))
    let altura = parseInt(prompt('Digite a altura'))
    area = base * altura
    alert("A area do triangulo é de:"+' '+area)
}
